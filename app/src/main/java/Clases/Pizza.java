package Clases;

public class Pizza {
    private int promoPizza;
    private int maxPizza;
    private int pizzaMaster;

    public Pizza() {
        this.promoPizza = 5900;
        this.maxPizza = 18500;
        this.pizzaMaster = 12990;
    }

    public int getPromoPizza() {
        return promoPizza;
    }

    public int getMaxPizza() {
        return maxPizza;
    }

    public int getPizzaMaster() {
        return pizzaMaster;
    }
}
